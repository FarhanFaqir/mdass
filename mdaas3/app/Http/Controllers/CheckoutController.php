    public function ajaxCheckout(Request $request)
    {
        $data = json_decode($request->data);

        // return $data->CID;
        $response = array();

        $response['reservation'] = Reservation::with('room','customer')->where('customer_id', $data->CID)->where('status', 1)->get();
        $response['service_order'] = ServiceOrder::where('reservation_id', $data->RID)->get();
        return $response;
    }
